var a = ["A","B","C","D"];
a.splice(1,2,"X","Y","Z"); // a->["A","X","Y","Z","D"] : 반환값 = ["B","C"]

var b = ["A","B","C","D"];
b.splice(2); // b->["A","B"] : 반환값 = ["C","D"]

var c = ["A","B","C","D"];
c.splice(-1); // a->["A","B","C"] : 반환값 = ["D"] (음수+배열길이) (-1+4=3번째 배열 이후 모든 배열요소 삭제)

var d = ["A","B","C","D"];
d.splice(1,0,"X","Y"); // a->["A","X","Y","B","C","D"] : 반환값 = []

